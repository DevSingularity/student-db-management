export const getStudentById = (studentId) => {
  return students.find((student) => student.id === studentId || student.studentId === studentId);
};

export const getStudentByGRNumber = (grNumber) => {
  return students.find((student) => student.grNumber === grNumber);
};

export const searchStudents = (query) => {
  const lowerQuery = query.toLowerCase();
  return students.filter(
    (student) =>
      student.fullName.toLowerCase().includes(lowerQuery) ||
      student.grNumber.toLowerCase().includes(lowerQuery) ||
      student.phoneNumber.includes(query) ||
      student.studentId.toLowerCase().includes(lowerQuery)
  );
};

export const getPaymentsByStudentId = (studentId) => {
  return payments.filter((payment) => payment.studentId === studentId);
};

export const authenticateUser = (employeeId, password) => {
  const employee = employees.find(
    (emp) => emp.employeeId === employeeId && emp.password === password
  );
  return employee || null;
};

